package given;

public class Node {

    private Node next;
    private Node pre;
    public Node() {
	
	
    }
    public Node getNext() {
        return next;
    }
    public Node getPre() {
        return pre;
    }
    public void setNext(Node next) {
        this.next = next;
    }
    public void setPre(Node pre) {
        this.pre = pre;
    }

    
}
